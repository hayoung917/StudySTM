  unsigned int FND_DATA_TBL[] = {0X3F00,0X0600,0X5B00,0X4F00,0X6600,0X6D00,0X7C00,0X0700,0X7F00,0X6700,0X7700,0X7C00,0X3900,0X5E00,0X7900,0X7100,0X0800,0X8000};
